from pydantic import BaseModel, conint, confloat
from typing import Optional


class Record(BaseModel):
    """
    Модель данных, представляющая запись с информацией о пациенте.

    Все числовые поля нормализованы в диапазон [0, 1], если не указано иное.
    Используются ограничения (confloat, conint) для обеспечения корректности входных данных.
    """

    Age: confloat(ge=0, le=1)
    """Возраст пациента, нормализованный в диапазоне [0, 1]."""

    Cholesterol: confloat(ge=0, le=1)
    """Уровень холестерина, нормализованный в диапазоне [0, 1]."""

    Heart_rate: confloat(ge=0, le=1)
    """Частота сердечных сокращений, нормализованный показатель."""

    Diabetes: conint(ge=0, le=1)
    """Признак наличия диабета (0 — отсутствует, 1 — присутствует)."""

    Family_History: conint(ge=0, le=1)
    """Признак семейной истории сердечных заболеваний (0 — нет, 1 — есть)."""

    Smoking: conint(ge=0, le=1)
    """Признак курения (0 — не курит, 1 — курит)."""

    Obesity: conint(ge=0, le=1)
    """Признак ожирения (0 — отсутствует, 1 — присутствует)."""

    Alcohol_Consumption: confloat(ge=0, le=1)
    """Уровень потребления алкоголя, нормализованный в диапазоне [0, 1]."""

    Exercise_Hours_Per_Week: confloat(ge=0, le=1)
    """Количество часов физической активности в неделю, нормализованное значение."""

    Diet: confloat(ge=0, le=3)
    """Оценка качества диеты, нормализованная в диапазоне [0, 3]."""

    Previous_Heart_Problems: conint(ge=0, le=1)
    """Признак предыдущих сердечных проблем (0 — нет, 1 — есть)."""

    Medication_Use: conint(ge=0, le=1)
    """Признак использования лекарств (0 — нет, 1 — да)."""

    Stress_Level: confloat(ge=0, le=1)
    """Уровень стресса, нормализованный в диапазоне [0, 1]."""

    Sedentary_Hours_Per_Day: confloat(ge=0, le=1)
    """Количество часов, проведённых в сидячем положении в день, нормализованное значение."""

    Income: confloat(ge=0, le=1)
    """Доход, нормализованный в диапазоне [0, 1]."""

    BMI: confloat(ge=0, le=1)
    """Индекс массы тела, нормализованный в диапазоне [0, 1]."""

    Triglycerides: confloat(ge=0, le=1)
    """Уровень триглицеридов, нормализованный в диапазоне [0, 1]."""

    Physical_Activity_Days_Per_Week: confloat(ge=0, le=1)
    """Количество дней физической активности в неделю, нормализованное значение."""

    Sleep_Hours_Per_Day: confloat(ge=0, le=1)
    """Количество часов сна в день, нормализованное значение."""

    Blood_sugar: confloat(ge=0, le=1)
    """Уровень сахара в крови, нормализованный в диапазоне [0, 1]."""

    CK_MB: confloat(ge=0, le=1)
    """Уровень фермента CK-MB, нормализованный в диапазоне [0, 1]."""

    Troponin: confloat(ge=0, le=1)
    """Уровень тропонина, нормализованный в диапазоне [0, 1]."""

    Gender: confloat(ge=0, le=1)
    """Пол пациента (0 — женский, 1 — мужской)."""

    Systolic_blood_pressure: confloat(ge=0, le=1)
    """Систолическое артериальное давление, нормализованное значение."""

    Diastolic_blood_pressure: confloat(ge=0, le=1)
    """Диастолическое артериальное давление, нормализованное значение."""

    id: Optional[int]
    """Идентификатор записи. Необязательный параметр."""
